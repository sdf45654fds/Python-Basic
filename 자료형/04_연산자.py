# + 더하기
print(5+2)
# - 빼기
print(5-2)
# * 곱하기
print(5*2)
# / 나누기
print(5/2)
# % 나머지
print(5%2)
# // 몫
print(5//2)
# ** 거듭제곱
print(5**2)
# == 같다
print(5==2)
# != 같지않다
print(5!=2)

'''논리 연산자'''
# and 둘다 참이면 True
print(3<5 and 7<5)
# or 하나라도 참이면 True
print(3<5 or 7<5)
# not 반전
print(not 3<5)

'''멤버 연산자'''
# in 포함
print('c' in 'cat')
# not in 미포함
print('c' not in 'cat')